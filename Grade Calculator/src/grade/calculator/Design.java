    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        double dbms,java,dsa,se,total,avg;
        String grade;
        
        dbms = Integer.parseInt(txtdbms.getText());
        java = Integer.parseInt(txtJava.getText());
        dsa = Integer.parseInt(txtDsa.getText());
        se = Integer.parseInt(txtSe.getText());
        
        total = dbms + java + dsa + se;
        
        avg = total/4;
        
        if(avg >= 90 && avg <= 100){
            grade = "O (Outstanding)";
        }
        else if(avg >= 80 && avg <= 89){
            grade = "A+ (Excellent)";
        }
        else if(avg >= 70 && avg <= 79){
            grade = "A (Very Good)";
        }
        else if(avg >= 60 && avg <=69){
            grade = "B+ (Good)";
        }
        else if(avg >= 50 && avg <=59){
            grade = "B (Above Average)";
        }
        else if(avg >= 40 && avg <=49){
            grade = "P (Pass)";
        }
        else{
            grade = "F (Fail)";
        }
        
        lbltotal.setText(String.valueOf(total));
        lblavg.setText(String.valueOf(avg));
        lblgrade.setText(grade);
    }//GEN-LAST:event_jButton1ActionPerformed

